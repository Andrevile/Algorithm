/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number[]}
 */
 var twoSum = function(nums, target) {
    for(var i=0; i<nums.length; i++){
        temp1=nums[i]
        for(var j=i+1; j<nums.length; j++){
            temp2=nums[j]
            if(temp1+temp2==target){
                return [i,j]
            }
        }
    }   
};
